    public static String getMimeType(String path)
    {
        String extension = getExtension(path);
        if (extension.isEmpty()) return DEFAULT_MIME_TYPE;

        Mime mime = MIME_TYPES.evaluate(extension);
        if (mime == null) return String.format("application/x-%s", extension);

        return mime.name;
    }

    private static String getExtension(String path)
    {
        if (path == null || path.trim().isEmpty()) return "";

        String extension = "";
        int i = path.lastIndexOf('.');
        if (i > 0 &&  i < path.length() - 1) extension = path.substring(i + 1).toLowerCase();
        return extension;
    }

    private record Mime(String extension, String name) implements Evaluator.Evaluable<String>
    {
        @Override
        public boolean toEvaluate(String extension)
        {
            return this.extension.equals(extension);
        }

    }
